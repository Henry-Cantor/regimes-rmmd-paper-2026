"""Phase-0 dataset wrapper for multi-machine STRONG-RMMD workflows."""
from __future__ import annotations

import json
from pathlib import Path
from typing import List, Optional

import torch
from torch.utils.data import Dataset

from strong_rmmd.data_loader import MultiMachineDataLoader
from strong_rmmd.geometry import LimiterGeometryEncoder
from strong_rmmd.resonance_frequencies import compute_resonance_frequencies


class MultiMachineDataset(Dataset):
    """Lightweight dataset built from loaded machine shots."""

    def __init__(self, machines_config, cdf_root_dir, split='train'):
        self.loader = MultiMachineDataLoader(cdf_root_dir=cdf_root_dir)
        self.machines_config = machines_config
        self.split = split
        self.machine_to_id = {cfg['name']: idx for idx, cfg in enumerate(machines_config)}
        self.samples = []
        for machine_cfg in machines_config:
            machine_name = machine_cfg['name']
            shots = self.loader.load_machine_shots(machine_name, n_shots=machine_cfg.get('n_shots'))
            self.samples.extend(shots)
        self.geometry_encoder = LimiterGeometryEncoder()

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        shot = self.samples[idx]
        harmonics = self.geometry_encoder.extract_harmonics_from_cdf(Path(shot.cdf_path), shot.machine_name)
        freq = compute_resonance_frequencies(shot.profiles, shot.machine_name, shot.metadata)
        return {
            'machine_name': shot.machine_name,
            'machine_id': self.machine_to_id.get(shot.machine_name, -1),
            'shot_id': shot.shot_id,
            'profiles': shot.profiles,
            'metadata': shot.metadata,
            'geometry': shot.geometry,
            'geom_harmonics': torch.tensor(harmonics, dtype=torch.float32),
            'omega_t': torch.tensor(freq['omega_t'], dtype=torch.float32),
            'omega_d': torch.tensor(freq['omega_d'], dtype=torch.float32),
        }


class ChunkedPhase0Dataset(Dataset):
    """Lazy reader for chunked Phase-0 batch artifacts.

    Expects a `machine_manifest.json` or `combined_manifest.json` generated by
    `STRONG_RMMD/data_io/build_dataset.py`. Each item is loaded from disk on demand.
    """

    def __init__(self, manifest_path: str | Path):
        self.manifest_path = Path(manifest_path)
        manifest = json.loads(self.manifest_path.read_text())

        self.batch_paths: List[Path] = []
        if 'machines' in manifest:
            for machine_name, machine_manifest in manifest['machines'].items():
                for batch in machine_manifest.get('batches', []):
                    self.batch_paths.append(Path(batch['path']))
        elif 'batches' in manifest:
            for batch in manifest.get('batches', []):
                self.batch_paths.append(Path(batch['path']))
        else:
            raise ValueError(f'Unsupported manifest structure: {self.manifest_path}')

        self.index: List[tuple[Path, int]] = []
        self._batch_lengths = {}
        for batch_path in self.batch_paths:
            payload = torch.load(batch_path, map_location='cpu')
            n_items = len(payload.get('data', []))
            self._batch_lengths[batch_path] = n_items
            for item_idx in range(n_items):
                self.index.append((batch_path, item_idx))

        self._cache_path: Optional[Path] = None
        self._cache_payload = None

    def __len__(self):
        return len(self.index)

    def _load_batch(self, batch_path: Path):
        if self._cache_path != batch_path:
            self._cache_payload = torch.load(batch_path, map_location='cpu')
            self._cache_path = batch_path
        return self._cache_payload

    def __getitem__(self, idx):
        batch_path, local_idx = self.index[idx]
        payload = self._load_batch(batch_path)
        data = payload['data'][local_idx]
        meta = payload['metadata'][local_idx]
        return {
            'data': data,
            'metadata': meta,
            'cdf_path': payload['cdf_paths'][local_idx] if 'cdf_paths' in payload else meta.get('cdf_path'),
            'batch_path': str(batch_path),
        }

